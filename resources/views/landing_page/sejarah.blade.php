            <h4>SEJARAH SINGKAT PERSATUAN GURU REPUBLIK INDONESIA(PGRI)</h4>
            <div class="col-lg-6 pt-4 pt-lg-0">
            <p> Semangat kebangsaan Indonesia telah lama tumbuh di kalangan guru-guru bangsa Indonesia. Organisasi perjuangan guru-guru pribumi pada zaman Belanda berdiri pada tahun 1912 dengan nama Persatuan Guru Hindia Belanda (PGHB). </p>
            <p> Organisasi ini bersifat unitaristik yang anggotanya terdiri dari para Guru Bantu, Guru Desa, Kepala Sekolah, dan Penilik Sekolah.  Dengan latar pendidikan yang berbeda-beda mereka umumnya bertugas di Sekolah Desa dan Sekolah Rakyat Angka Dua. </p>
            <p> Tidak mudah bagi PGHB memperjuangkan nasib para anggotanya yang memiliki pangkat, status sosial dan latar belakang pendidikan yang berbeda. Sejalan dengan keadaan itu maka di samping PGHB berkembang pula organisasi guru baru antara lain Persatuan Guru Bantu (PGB), Perserikatan Guru Desa (PGD), Persatuan Guru Ambachtsschool (PGAS), Perserikatan Normaalschool (PNS), Hogere Kweekschool Bond (HKSB), disamping organisasi guru yang bercorak keagamaan, kebangsaan  atau lainnya seperti Christelijke Onderwijs Vereneging (COV), Katolieke Onderwijsbond (KOB), Vereneging Van Muloleerkrachten (VVM), dan Nederlands Indische Onderwijs Genootschap (NIOG) yang beranggotakan semua guru tanpa membedakan golongan agama. </p>
            <p> Kesadaran kebangsaan dan semangat perjuangan yang sejak lama tumbuh,  mendorong para guru pribumi memperjuangkan persamaan hak dan posisi dengan pihak  Belanda. Hasilnya antara lain adalah Kepala HIS yang dulu selalu dijabat oleh orang Belanda, satu per satu pindah ke tangan orang Indonesia. Semangat perjuangan ini makin berkobar dan memuncak pada kesadaran dan cita-cita kemerdekaan. Perjuangan guru tidak lagi perjuangan perbaikan nasib, tidak lagi perjuangan  kesamaan hak dan posisi dengan Belanda, tetapi telah memuncak menjadi perjuangan nasional dengan teriak “merdeka”. </p>
            <p> Pada tahun 1932 nama Persatuan Guru Hindia Belanda (PGHB) diubah menjadi Persatuan Guru Indonesia (PGI). Perubahan nama ini mengejutkan pemerintah Belanda, karena kata “Indonesia” yang mencerminkan semangat kebangsaan sangat tidak disenangi oleh Belanda. Sebaliknya kata “Indonesia” ini sangat didambakan oleh guru dan bangsa Indonesia. </p>
            <p> Pada zaman pendudukan Jepang segala organisasi dilarang, sekolah ditutup, Persatuan Guru Indonesia (PGI) tidak dapat lagi melakukan aktivitas. </p>
            <p> Semangat proklamasi 17 Agustus 1945 menjiwai penyelenggaraan Kongres Guru Indonesia pada tanggal 24-25 November 1945 di Surakarta. Melalui kongres ini segala organisasi dan kelompok guru yang didasarkan atas perbedaan tamatan, lingkungan pekerjaan, lingkungan daerah, politik, agama dan suku, sepakat dihapuskan. Mereka adalah –guru-guru yang aktif mengajar, pensiunan guru yang aktif berjuang, dan pegawai pendidikan Republik Indonesia yang baru dibentuk. Mereka bersatu untuk Negara Kesatuan Republik Indonesia. Di dalam kongres inilah, pada tanggal 25 November 1945 –seratus hari setelah proklamasi kemerdekaan Republik Indonesia–  Persatuan Guru Republik Indonesia (PGRI) didirikan. </p>
            <p> Dengan semangat pekik “merdeka” yang bertalu-talu, di tengah bau mesiu pemboman oleh tentara Inggris atas studio RRI Surakarta, mereka serentak bersatu untuk mengisi kemerdekaan dengan tiga tujuan : </p>
            <p> 1. Mempertahankan dan menyempurnakan Republik Indonesia. </p>
            <p> 2. Mempertinggi tingkat pendidikan dan pengajaran sesuai dengan dasar-dasar kerakyatan. </p>
            <p> 3. Membela hak dan nasib buruh umumnya,guru pada khususnya.</p>
            <p> Sejak Kongres Guru Indonesia itu, semua guru Indonesia menyatakan dirinya bersatu di dalam wadah Persatuan Guru Republik Indonesia (PGRI). </p>
            <p> Jiwa pengabdian, tekad perjuangan, dan semangat persatuan dan kesatuan PGRI yang dimiliki secara historis terus dipupuk  dalam mempertahankan dan mengisi kemerdekaan Negara Kesatuan Republik Indonesia. Dalam rona dan dinamika politik yang sangat dinamis, Persatuan  Guru Republik Indonesia (PGRI) tetap setia dalam pengabdiannya sebagai organisasi perjuangan, organisasi profesi, dan organisasi ketenagakerjaan, yang bersifat unitaristik, dan independen. </p>
            <p> Untuk itulah , sebagai penghormatan  kepada guru, pemerintah Republik Indonesia dengan Keputusan Presiden Nomor 78 Tahun 1994, menetapkan hari lahir PGRI tanggal 25 November sebagai Hari Guru Nasional , dan diperingati setiap tahun. </p>
            Semoga PGRI, guru dan bangsa Indonesia tetap jaya dalam wadah Negara Kesatuan Republik Indonesia.
            </p>
